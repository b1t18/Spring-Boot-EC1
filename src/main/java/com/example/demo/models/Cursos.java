package com.example.demo.models;

public class Cursos {
	private String codCurso;
	private String nomCurso;
	private String profCargo;
	private int horasDictadas;
	
	
	public String getCodCurso() {
		return codCurso;
	}
	public void setCodCurso(String codCurso) {
		this.codCurso = codCurso;
	}
	public String getNomCurso() {
		return nomCurso;
	}
	public void setNomCurso(String nomCurso) {
		this.nomCurso = nomCurso;
	}
	public String getProfCargo() {
		return profCargo;
	}
	public void setProfCargo(String profCargo) {
		this.profCargo = profCargo;
	}
	public int getHorasDictadas() {
		return horasDictadas;
	}
	public void setHorasDictadas(int horasDictadas) {
		this.horasDictadas = horasDictadas;
	}
	
	
	

}
